const rainCanvas = document.getElementById("rainCanvas");
const rainCtx = rainCanvas.getContext("2d");
rainCanvas.width = window.innerWidth;
rainCanvas.height = window.innerHeight;
let bgDrops = [];
for (let i=0;i<150;i++) bgDrops.push({
  x: Math.random()*rainCanvas.width, y: Math.random()*rainCanvas.height,
  length: Math.random()*20+10, speed: Math.random()*15+10, opacity: Math.random()*0.4+0.1
});
function drawBgRain() {
  rainCtx.clearRect(0,0,rainCanvas.width,rainCanvas.height);
  rainCtx.lineWidth=1;
  bgDrops.forEach(d=>{
    rainCtx.beginPath(); rainCtx.moveTo(d.x,d.y); rainCtx.lineTo(d.x,d.y+d.length);
    rainCtx.strokeStyle=`rgba(255,255,255,${d.opacity})`; rainCtx.stroke();
    d.y+=d.speed;
    if (d.y>rainCanvas.height) { d.y=-d.length; d.x=Math.random()*rainCanvas.width; }
  });
  requestAnimationFrame(drawBgRain);
}
window.addEventListener("resize",()=>{
  rainCanvas.width=window.innerWidth; rainCanvas.height=window.innerHeight;
  bgDrops=[]; for (let i=0;i<150;i++) bgDrops.push({ x:Math.random()*rainCanvas.width, y:Math.random()*rainCanvas.height, length:Math.random()*20+10, speed:Math.random()*15+10, opacity:Math.random()*0.4+0.1 });
});
drawBgRain();

