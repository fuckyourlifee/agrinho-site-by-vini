const gameCanvas = document.getElementById("gameCanvas");
const gameCtx = gameCanvas.getContext("2d");
const scoreSpan = document.getElementById("scoreDisplay");
let bucket = { x: gameCanvas.width/2-40, y: gameCanvas.height-50, width:80, height:20 };
let raindrops = [], gameScore = 0, gameRunning = false;

function spawnRaindrop() {
  return { x: Math.random()*(gameCanvas.width-10)+5, y:-10, radius:Math.random()*4+2, speed:Math.random()*3+2 };
}

function drawGame() {
  gameCtx.clearRect(0,0,gameCanvas.width,gameCanvas.height);
  gameCtx.fillStyle="#8B4513"; gameCtx.fillRect(bucket.x,bucket.y,bucket.width,bucket.height);
  gameCtx.fillStyle="#A0522D"; gameCtx.fillRect(bucket.x+10,bucket.y-10,bucket.width-20,10);
  gameCtx.fillStyle="#4fc3f7";
  raindrops.forEach(d=>{ gameCtx.beginPath(); gameCtx.arc(d.x,d.y,d.radius,0,Math.PI*2); gameCtx.fill(); });
  gameCtx.fillStyle="white"; gameCtx.font="16px Inter"; gameCtx.fillText("Score: "+gameScore,10,25);
}

function updateGame() {
  if (!gameRunning) return;
  for (let i=raindrops.length-1;i>=0;i--) {
    let d=raindrops[i]; d.y+=d.speed;
    if (d.y+d.radius>=bucket.y && d.y-d.radius<=bucket.y+bucket.height && d.x>=bucket.x && d.x<=bucket.x+bucket.width) {
      raindrops.splice(i,1); gameScore++; scoreSpan.textContent=gameScore;
    } else if (d.y-d.radius>gameCanvas.height) { raindrops.splice(i,1); }
  }
  if (Math.random()<0.03) raindrops.push(spawnRaindrop());
  drawGame();
}

function gameLoop() { updateGame(); if (gameRunning) requestAnimationFrame(gameLoop); }

document.addEventListener("keydown", e => {
  if (!gameRunning) return;
  if (e.key==="ArrowLeft") bucket.x=Math.max(0,bucket.x-20);
  if (e.key==="ArrowRight") bucket.x=Math.min(gameCanvas.width-bucket.width,bucket.x+20);
});

gameCanvas.addEventListener("mousemove", e => {
  if (!gameRunning) return;
  let rect=gameCanvas.getBoundingClientRect();
  let x=e.clientX-rect.left;
  bucket.x=Math.max(0,Math.min(gameCanvas.width-bucket.width, x-bucket.width/2));
});

gameCanvas.addEventListener("touchmove", e => {
  e.preventDefault();
  if (!gameRunning) return;
  let rect=gameCanvas.getBoundingClientRect();
  let x=e.touches[0].clientX-rect.left;
  bucket.x=Math.max(0,Math.min(gameCanvas.width-bucket.width, x-bucket.width/2));
});

function startGame() {
  if (gameRunning) return;
  gameRunning=true; gameScore=0; raindrops=[]; scoreSpan.textContent="0";
  bucket.x=gameCanvas.width/2-40;
  drawGame(); requestAnimationFrame(gameLoop);
}

function stopGame() { gameRunning=false; raindrops=[]; }
document.getElementById("restartGame").addEventListener("click",()=>{ stopGame(); startGame(); });

const observer = new MutationObserver(() => {
  if (document.getElementById("page-game").classList.contains("active")) startGame();
  else stopGame();
});
observer.observe(document.getElementById("page-game"),{attributes:true,attributeFilter:['class']});

